
package myjavaproject;


public class Switch {
    public static void main(String args[]){
        int dice =67;
        switch(dice){
            
            case 1:{
                System.out.println("One is output");
                break;
            }
            case 2:{
                System.out.println("2 is output");
                break;
            }
            case 3:{
                System.out.println("3 is output");
                break;
            }
            case 4:{
                System.out.println("4 is output");
                break;
            }
            case 5 :{
                System.out.println("5 is output");
                break;
            }
            default:{
                System.out.println("invalid output");
                break;
            }
            
                
            
            
        }
    }
    
}
