
package myjavaproject;


public class TypesOfVaribles {
    
    static byte a;
    static short b;
    static  int c;      
    static  long d;  
    static float e;
    static double f;
    static boolean g;
    static char h;
    static String i;
public static void main (String args[]){
    System.out.println("byte default value "+a);
    System.out.println("short default value "+b);
    System.out.println("int  default value "+c);
    System.out.println("long default value "+d);
    System.out.println("float default value "+e);
    System.out.println("double default value "+f);
    System.out.println("boolean default value "+g);
    System.out.println("char default value "+h);
    System.out.println("String default value "+i);


}
    
}
